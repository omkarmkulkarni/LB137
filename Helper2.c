//
// Function name: Maximum
// Input: Integer, Integer
// Output: Integer
// Description: It is used to find out the largest number
// Date: 2/23/2021
// Author: Omkar Maltesh Kulkarni
//
////////////////////////////////////////////////

int Maximum(int iValue1, int iValue2)
{
    if(iValue1 > iValue2)
    {
        return iValue1;
    }
    else
    {
        return iValue2;
    }
}

////////////////////////////////////////////////
//
// Function name: Minimum
// Input: Integer, Integer
// Output: Integer
// Description: It is used to find out the smallest number
// Date: 2/23/2021
// Author: Omkar Maltesh Kulkarni
//
////////////////////////////////////////////////

int Minimum(int iValue1, int iValue2)
{
    if(iValue1 < iValue2)
    {
        return iValue1;
    }
    else
    {
        return iValue2;
    }
}